package com.example.user.bulletfalls.GlobalUsage.Supporters;

public class RomeLettersConverter {



    public String convert(int i)
    {
        switch(i)
        {
            case 1: return "I";
            case 2: return "II";
            case 3: return "III";
            case 4: return "IV";
            case 5: return "V";
            case 6: return "VI";
            case 7: return "VII";
        }
        return " ";
    }
}
